        function waitForElement(
            selector,
            trigger,
            delayInterval = 50,
            delayTimeout = 15000,
        ) {
            var interval = setInterval(function () {
                if (
                    document &&
                    document.querySelector(selector) &&
                    document.querySelectorAll(selector).length > 0
                ) {
                    clearInterval(interval);
                    trigger(document.querySelector(selector));
                }
            }, delayInterval);
            setTimeout(function () {
                clearInterval(interval);
            }, delayTimeout);
        }

